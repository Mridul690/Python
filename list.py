# Creating a multi-data types list
# List are created using []
c = [45, 'Danger', False, 32.2]
print(c)

# List can be accessed through the index number
print(c[3])

# List values can be changed
c[2] = True
print(c)

# List Slicing
# list[i:n]
# From ith position to (n-1)th position
friends = ["Harry", "Sam", "Divya", "Sty", 45]
print(friends[0:4])
